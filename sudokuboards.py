def Format(board):
    horizontal_line = "-------------------------"
    print(horizontal_line)
    for i in range(9):
        row = "| " + " ".join(f"{board[i][j] if board[i][j] is not None else ' '}" for j in range(9))
        print(row[0:7] + "|" + row[7:14] + "|" + row[14:])
        if (i + 1) % 3 == 0:
            print(horizontal_line)
    print("\n")


